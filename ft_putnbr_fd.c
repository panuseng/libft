#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fb('-');
		if (n == -2147483648)
		{
			ft_putchar_fb('2');
			n = 147483648;
		}
		else
			n *= -1;
	}
	if (n > 9)
		ft_putnbr_fb(n / 10);
	ft_putchar_fb(n % 10 + '0');
}
